import UIKit

extension UIColor {
    enum Custom {
        static var black: UIColor? { UIColor(named: "black") }
        static var blueButton: UIColor? { UIColor(named: "blueButton") }
        static var blueButtonDayDark: UIColor? { UIColor(named: "blueButtonDayDark") }
        static var grayBackground: UIColor? { UIColor(named: "grayBackground") }
        static var grayText: UIColor? { UIColor(named: "grayText") }
        static var lightGrayText: UIColor? { UIColor(named: "lightGrayText") }
        static var whiteTextTost: UIColor? { UIColor(named: "whiteTextTost") }
        static var divider: UIColor? { UIColor(named: "divider") }
    }
}
